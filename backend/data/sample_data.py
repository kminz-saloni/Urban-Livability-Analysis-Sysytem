def get_city_metrics_dict() -> Dict[str, Dict]:
    """Get sample data as dictionary indexed by city name"""
    return {city['city_name']: city for city in CITY_METRICS}


def get_cities_by_state(state: str) -> List[Dict]:
    """Get cities for a specific state"""
    return [city for city in CITY_METRICS if city['state'] == state]


def get_cities_by_tier(tier: str) -> List[Dict]:
    """Get cities for a specific tier"""
    return [city for city in CITY_METRICS if city['tier'] == tier]
